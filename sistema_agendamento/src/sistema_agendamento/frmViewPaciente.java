    public String maskTel(String tel){
        if(tel == null || tel.length() != 11){
            return tel;
        }else{
        return "(" + tel.substring(0, 2) + ") " + 
           tel.substring(2, 3) + " " + 
           tel.substring(3, 7) + "-" + 
           tel.substring(7, 11);    
        }  
    }

