    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        try {
                PacienteDAO objPacienteDAO = new PacienteDAO();
                DefaultTableModel model = (DefaultTableModel) TabelaEspecialidades.getModel();
                model.setNumRows(0);
                ArrayList<PacienteDTO> lista = objPacienteDAO.ListarPacientes();
                for (int num = 0; num < lista.size(); num++) {
                    model.addRow(new Object[]{
                    maskCpf(lista.get(num).getCpfPac()),
                    lista.get(num).getNomePac(),
                    lista.get(num).getEndPac(),
                    maskTel(lista.get(num).getTelPac()),});
                }
            } catch (Exception e) {
            } 
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltActionPerformed
        // TODO add your handling code here:
        int setar = TabelaEspecialidades.getSelectedRow();
        String cpf = TabelaEspecialidades.getModel().getValueAt(setar, 0).toString();
        cpf = sanitizeCpf(cpf);
        String nome = TabelaEspecialidades.getModel().getValueAt(setar, 1).toString();
        String tel = TabelaEspecialidades.getModel().getValueAt(setar, 2).toString();
        String end = TabelaEspecialidades.getModel().getValueAt(setar, 3).toString();
        new frmPaciente(cpf, nome, tel, end).setVisible(true);
    }//GEN-LAST:event_btnAltActionPerformed

    private void btnExcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcActionPerformed
        // TODO add your handling code here:
        int setar = TabelaEspecialidades.getSelectedRow();
        
        String cpf = sanitizeCpf(TabelaEspecialidades.getModel().getValueAt(setar, 0).toString());
        
        PacienteDTO objPacienteDTO = new PacienteDTO();
        objPacienteDTO.setCpfPac(cpf);
        
        PacienteDAO objPacienteDAO = new PacienteDAO();
        objPacienteDAO.ExcluirPaciente(objPacienteDTO);
        
        btnAtualizar.doClick();
    }//GEN-LAST:event_btnExcActionPerformed

