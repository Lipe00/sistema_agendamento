    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        try {
            EspecialidadeDAO objEspecialidadeDAO = new EspecialidadeDAO();
            DefaultTableModel model = (DefaultTableModel) TabelaEspecialidades.getModel();
            model.setNumRows(0);
            ArrayList<EspecialidadeDTO> lista = objEspecialidadeDAO.ListarEspecialidades();
            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getIdEsp(),
                    lista.get(num).getNomeEsp(),});
        }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

