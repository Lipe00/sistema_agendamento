    private void btnAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltActionPerformed
        // TODO add your handling code here:
        int setar = TabelaEspecialidades.getSelectedRow();
        if(setar == -1){
            JOptionPane.showMessageDialog(null, "Por favor selecione o registro que deseja alterar!", "Erro", JOptionPane.ERROR_MESSAGE);
        }else{
            int id = (int) TabelaEspecialidades.getModel().getValueAt(setar, 0);
            String nome = TabelaEspecialidades.getModel().getValueAt(setar, 1).toString();
            new frmEspecialidade(id, nome).setVisible(true);
        }
    }//GEN-LAST:event_btnAltActionPerformed

    private void btnExcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcActionPerformed
        // TODO add your handling code here:
        int setar = TabelaEspecialidades.getSelectedRow();
        if(setar == -1){
            JOptionPane.showMessageDialog(null, "Por favor selecione o registro que deseja apagar!", "Erro", JOptionPane.ERROR_MESSAGE);
        }else{
            int id = (int) TabelaEspecialidades.getModel().getValueAt(setar, 0);
            
            EspecialidadeDTO objEspecialidadeDTO = new EspecialidadeDTO();
            objEspecialidadeDTO.setIdEsp(id);

            EspecialidadeDAO objEspecialidadeDAO = new EspecialidadeDAO();
            objEspecialidadeDAO.ExcluirEspecialidade(objEspecialidadeDTO);
            
            btnAtualizar.doClick();
        }
    
    }//GEN-LAST:event_btnExcActionPerformed

