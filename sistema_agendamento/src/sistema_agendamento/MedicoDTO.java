public class MedicoDTO{
    private int espMed;
    private String cpfMed, nomeMed, telMed, EndMed, crmMed, nomeEspMed;

    public String getNomeEspMed() {
        return nomeEspMed;
    }

    public void setNomeEspMed(String nomeEspMed) {
        this.nomeEspMed = nomeEspMed;
    }

    public String getCpfMed() {
        return cpfMed;
    }

    public void setCpfMed(String cpfMed) {
        this.cpfMed = cpfMed;
    }

    public String getTelMed() {
        return telMed;
    }

    public void setTelMed(String telMed) {
        this.telMed = telMed;
    }

    public String getNomeMed() {
        return nomeMed;
    }

    public void setNomeMed(String nomeMed) {
        this.nomeMed = nomeMed;
    }

    public String getEndMed() {
        return EndMed;
    }

    public void setEndMed(String EndMed) {
        this.EndMed = EndMed;
    }
    

    public int getEspMed() {
        return espMed;
    }

    public void setEspMed(int espMed) {
        this.espMed = espMed;
    }

    public String getCrmMed() {
        return crmMed;
    }

    public void setCrmMed(String crmMed) {
        this.crmMed = crmMed;
    }
    
}
