    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        try {
            MedicoDAO objMedicoDAO = new MedicoDAO();
            DefaultTableModel model = (DefaultTableModel) TabelaMedicos.getModel();
            model.setNumRows(0);
            ArrayList<MedicoDTO> lista = objMedicoDAO.ListarMedicos();
            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    maskCpf(lista.get(num).getCpfMed()),
                    lista.get(num).getNomeMed(),
                    lista.get(num).getEndMed(),
                    maskTel(lista.get(num).getTelMed()),
                    lista.get(num).getNomeEspMed(),
                    lista.get(num).getCrmMed(),});
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

