    private void btnAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltActionPerformed
        // TODO add your handling code here:
        int setar = TabelaMedicos.getSelectedRow();
        String cpf = TabelaMedicos.getModel().getValueAt(setar, 0).toString();
        cpf = sanitizeCpf(cpf);
        String nome = TabelaMedicos.getModel().getValueAt(setar, 1).toString();
        String tel = TabelaMedicos.getModel().getValueAt(setar, 2).toString();
        String end = TabelaMedicos.getModel().getValueAt(setar, 3).toString();
        String esp = TabelaMedicos.getModel().getValueAt(setar, 4).toString();
        String crm = TabelaMedicos.getModel().getValueAt(setar, 5).toString();
        new frmMedico(cpf, nome, tel, end, crm, esp).setVisible(true);
    }//GEN-LAST:event_btnAltActionPerformed

    private void btnExcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcActionPerformed
        // TODO add your handling code here:
        int setar = TabelaMedicos.getSelectedRow();

        String cpf = sanitizeCpf(TabelaMedicos.getModel().getValueAt(setar, 0).toString());

        MedicoDTO objMedicoDTO = new MedicoDTO();
        objMedicoDTO.setCpfMed(cpf);

        MedicoDAO objMedicoDAO = new MedicoDAO();
        objMedicoDAO.ExcluirMedico(objMedicoDTO);

        btnAtualizar.doClick();
    }//GEN-LAST:event_btnExcActionPerformed

